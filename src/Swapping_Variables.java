public class Swapping_Variables {

    public static void main(String[] args){
        String x = "cat";
        String y = "dog";
        String temp =null;
        temp = y;
         y = x;
        x = temp;

        //use temp to store a variable

        // if you put x=y then x will display the value of y


        System.out.println("x: "+x);
        System.out.println("y: "+y);
    }
}
